package com.task.ctrlstmt.may252020;

public class TaskDoWhileLoop {
	public void doWhile() {
		int h=8 ;
		do
		{
			System.out.println("The value of h :" +h);
			h++;
		}
		while(h<45);
		
		
	}

	public static void main(String[] args) {
		TaskDoWhileLoop twh=new TaskDoWhileLoop();
		twh.doWhile();
		

	}

}
