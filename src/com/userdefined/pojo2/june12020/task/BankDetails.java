package com.userdefined.pojo2.june12020.task;

public class BankDetails {
	private String bankAccountHolder;
	private String IFSC;
	private String bankBranch;
	private float depostiedaAmount;
	private float withDrawAmount;
	private float remainingAmount;
	
	public String getBankAccountHolder() {
		return bankAccountHolder;
	}
	public void setBankAccountHolder(String bankAccountHolder) {
		this.bankAccountHolder = bankAccountHolder;
	}
	public String getIFSC() {
		return IFSC;
	}
	public void setIFSC(String iFSC) {
		IFSC = iFSC;
	}
	public String getBankBranch() {
		return bankBranch;
	}
	public void setBankBranch(String bankBranch) {
		this.bankBranch = bankBranch;
	}
	public float getDepostiedaAmount() {
		return depostiedaAmount;
	}
	public void setDepostiedaAmount(float depostiedaAmount) {
		this.depostiedaAmount = depostiedaAmount;
	}
	public float getWithDrawAmount() {
		return withDrawAmount;
	}
	public void setWithDrawAmount(float withDrawAmount) {
		this.withDrawAmount = withDrawAmount;
	}
	public float getRemainingAmount() {
		return remainingAmount;
	}
	public void setRemainingAmount(float remainingAmount) {
		this.remainingAmount = remainingAmount;
	}
	
	
	

}
